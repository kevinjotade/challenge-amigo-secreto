document.addEventListener("DOMContentLoaded", function () {
    const listaAmigos = [];
    const inputNombre = document.getElementById("amigo");
    const listaHTML = document.getElementById("listaAmigos");
    const resultadoHTML = document.getElementById("resultado");

    document.querySelector(".button-add").addEventListener("click", function () {
        const nombre = inputNombre.value.trim();

        if (!nombre) {
            alert("Por favor, ingrese un nombre antes de añadirlo.");
            return;
        }

        if (!listaAmigos.includes(nombre)) {
            listaAmigos.push(nombre);
            actualizarLista();
            inputNombre.value = ""; // Limpiar el campo después de agregar
        }
    });

    function actualizarLista() {
        listaHTML.innerHTML = ""; // Limpiar lista antes de actualizar

        listaAmigos.forEach((amigo) => {
            const li = document.createElement("li");
            li.textContent = amigo;
            listaHTML.appendChild(li);
        });
    }

    document.querySelector(".button-draw").addEventListener("click", function () {
        if (listaAmigos.length === 0) {
            alert("No hay nombres en la lista. Agrega al menos un amigo.");
            return;
        }

        const indiceSorteado = Math.floor(Math.random() * listaAmigos.length);
        const amigoSorteado = listaAmigos[indiceSorteado];

        resultadoHTML.innerHTML = `<li>${amigoSorteado}</li>`;
        console.log("El amigo sorteado es: " + amigoSorteado);
    });
});


